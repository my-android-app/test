package com.shoushuzhitongche.app.view.mine.bean;

public class UserInfoEntity {

	private Boolean doctorCerts;
	private Boolean isProfile;
	private String name;
	private Boolean verified;
	private Boolean teamDoctor;
	public Boolean getDoctorCerts() {
		return doctorCerts;
	}
	public void setDoctorCerts(Boolean doctorCerts) {
		this.doctorCerts = doctorCerts;
	}
	public Boolean getIsProfile() {
		return isProfile;
	}
	public void setIsProfile(Boolean isProfile) {
		this.isProfile = isProfile;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Boolean getVerified() {
		return verified;
	}
	public void setVerified(Boolean verified) {
		this.verified = verified;
	}
	public Boolean getTeamDoctor() {
		return teamDoctor;
	}
	public void setTeamDoctor(Boolean teamDoctor) {
		this.teamDoctor = teamDoctor;
	}
	
	
	
}
